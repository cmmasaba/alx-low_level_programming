#include<stdio.h>
/**
 * main - prints the various sizes of variables
 * Return: prints 0 if no error is encountered
 */
int main(void)
{
	printf("Size of a char :%lu\n", sizeof(char) "byte(s\n)");
	printf("Size of an int :%lu\n", sizeof(int) "byte(s)\n");
	printf("Size of a long int :%lu\n", sizeof(double) "byte(s)\n");
	printf("Size of a long long int :%lu\n", sizeof(double) "byte(s)\n");
	printf("Size of a float :%lu\n", sizeof(float) "byte(s)\n");
	return (0);
}
