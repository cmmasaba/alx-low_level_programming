#include "3-calc.h"

/**
 * op_add - performs addition operation on two values
 * @a: the first value
 * @b: the second value
 *
 * Return: the answer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction operation on two values
 * @a: the first value
 * @b: the second value
 *
 * Return: the answer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication operation on two values
 * @a: the first value
 * @b: the second value
 *
 * Return: the answer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division operation on two values
 * @a: the first value
 * @b: the second value
 *
 * Return: the answer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  performs modulo operation on two values
 * @a: the first value
 * @b: the second value
 *
 * Return: the answer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
